#!/usr/bin/env python
from __future__ import annotations

import argparse
import json
from pathlib import Path

import pandas as pd


COND_LABELS = {
    "gpu1_sd_inpaint": "Verified SDXL inpainting",
    "gpu1_gan_curated": "Curated GAN",
}

MODEL_LABELS = {
    "monai_unet": "MONAI U-Net",
    "smp_deeplabv3plus_resnet50": "DeepLabV3+",
    "smp_fpn_effb3": "FPN",
    "smp_unet_resnet34": "U-Net",
    "smp_unetpp_effb4": "U-Net++",
}


def parse_run_id(run_id: str) -> tuple[str, str, int]:
    condition, model, seed_text = run_id.rsplit("__", 2)
    return condition, model, int(seed_text.replace("seed", ""))


def load_best_val(history_path: Path) -> float | None:
    if not history_path.exists():
        return None
    vals = []
    for line in history_path.read_text(encoding="utf-8").splitlines():
        if not line.strip():
            continue
        vals.append(float(json.loads(line).get("dice_mean", 0.0)))
    return max(vals) if vals else None


def fmt(value: float | None) -> str:
    if value is None or pd.isna(value):
        return "--"
    return f"{float(value):.3f}"


def pm(mean: float, sd: float | None) -> str:
    if sd is None or pd.isna(sd):
        return fmt(mean)
    return f"{fmt(mean)} $\\pm$ {fmt(sd)}"


def table_rows(frame: pd.DataFrame, label_cols: list[str]) -> str:
    rows = []
    for row in frame.itertuples(index=False):
        labels = [getattr(row, col) for col in label_cols]
        rows.append(
            "    "
            + " & ".join(labels)
            + f" & {int(row.runs)} & {pm(row.dice_mean, row.dice_sd)}"
            + f" & {pm(row.iou_mean, row.iou_sd)}"
            + f" & {fmt(row.boundary_f1_mean)}"
            + f" & {fmt(row.best_val_dice_mean)} \\\\"
        )
    return "\n".join(rows)


def build_latex(by_condition: pd.DataFrame, by_model: pd.DataFrame, runs: pd.DataFrame) -> str:
    if runs.empty:
        return "% No final gpu1_sd_gan C3 results are available yet.\n"
    top = runs.sort_values("dice_mean", ascending=False).iloc[0]
    return rf"""% Auto-generated by benchmark/scripts/summarize_gpu1_sd_gan_results.py

\subsection{{Verified Generator Segmentation Results}}

The verified-generator follow-up evaluated completed checkpoints trained on synthetic sources that passed explicit generation quality control. The strongest completed verified-generator run was {top.condition_label} with {top.model_label} at seed {int(top.seed)}, reaching held-out C3-clean Dice {fmt(top.dice_mean)}, IoU {fmt(top.iou_mean)}, and boundary F1 {fmt(top.boundary_f1_mean)}. These results are reported separately from earlier baselines because the generator audit removed no-op or low-quality synthetic outputs before segmentation training.

\begin{{table}}[H]
  \centering
  \caption{{Held-out C3-clean performance for verified generated-data training conditions. Values are mean $\pm$ standard deviation over completed checkpoints.}}
  \label{{tab:verified-generator-condition-results}}
  \begin{{tabular}}{{lrrrrr}}
    \toprule
    \textbf{{Training condition}} & \textbf{{Runs}} & \textbf{{Dice}} & \textbf{{IoU}} & \textbf{{Boundary F1}} & \textbf{{Best val Dice}} \\
    \midrule
{table_rows(by_condition, ["condition_label"])}
    \bottomrule
  \end{{tabular}}
\end{{table}}

\begin{{table}}[H]
  \centering
  \caption{{Held-out C3-clean performance by verified generated-data condition and segmentation model. Values are mean $\pm$ standard deviation over completed seeds.}}
  \label{{tab:verified-generator-model-results}}
  \begin{{tabular}}{{llrrrrr}}
    \toprule
    \textbf{{Condition}} & \textbf{{Model}} & \textbf{{Runs}} & \textbf{{Dice}} & \textbf{{IoU}} & \textbf{{Boundary F1}} & \textbf{{Best val Dice}} \\
    \midrule
{table_rows(by_model, ["condition_label", "model_label"])}
    \bottomrule
  \end{{tabular}}
\end{{table}}
"""


def main() -> None:
    parser = argparse.ArgumentParser()
    parser.add_argument("--run-root", default="results/runs/gpu1_sd_gan")
    parser.add_argument("--out-dir", default="results/reports/gpu1_sd_gan")
    args = parser.parse_args()

    run_root = Path(args.run_root)
    out_dir = Path(args.out_dir)
    out_dir.mkdir(parents=True, exist_ok=True)

    rows = []
    for summary_path in sorted(run_root.glob("*/c3_clean_metrics.summary.json")):
        run_id = summary_path.parent.name
        condition, model, seed = parse_run_id(run_id)
        if condition not in COND_LABELS:
            continue
        payload = json.loads(summary_path.read_text(encoding="utf-8"))
        rows.append(
            {
                "run_id": run_id,
                "condition": condition,
                "condition_label": COND_LABELS.get(condition, condition),
                "model": model,
                "model_label": MODEL_LABELS.get(model, model),
                "seed": seed,
                "best_val_dice": load_best_val(summary_path.parent / "history.jsonl"),
                **payload,
            }
        )

    runs = pd.DataFrame(rows)
    if runs.empty:
        (out_dir / "verified_generator_results.tex").write_text(
            "% No final gpu1_sd_gan C3 results are available yet.\n",
            encoding="utf-8",
        )
        raise SystemExit("No final C3 summary files found.")

    runs = runs.sort_values(["condition", "model", "seed"])
    runs.to_csv(out_dir / "verified_generator_run_metrics.csv", index=False)

    by_condition = (
        runs.groupby(["condition", "condition_label"], as_index=False)
        .agg(
            runs=("run_id", "count"),
            dice_mean=("dice_mean", "mean"),
            dice_sd=("dice_mean", "std"),
            iou_mean=("iou_mean", "mean"),
            iou_sd=("iou_mean", "std"),
            boundary_f1_mean=("boundary_f1_mean", "mean"),
            best_val_dice_mean=("best_val_dice", "mean"),
        )
        .sort_values("dice_mean", ascending=False)
    )
    by_condition.to_csv(out_dir / "verified_generator_by_condition.csv", index=False)

    by_model = (
        runs.groupby(["condition", "condition_label", "model", "model_label"], as_index=False)
        .agg(
            runs=("run_id", "count"),
            dice_mean=("dice_mean", "mean"),
            dice_sd=("dice_mean", "std"),
            iou_mean=("iou_mean", "mean"),
            iou_sd=("iou_mean", "std"),
            boundary_f1_mean=("boundary_f1_mean", "mean"),
            best_val_dice_mean=("best_val_dice", "mean"),
        )
        .sort_values(["condition", "dice_mean"], ascending=[True, False])
    )
    by_model.to_csv(out_dir / "verified_generator_by_model.csv", index=False)

    latex = build_latex(by_condition, by_model, runs)
    (out_dir / "verified_generator_results.tex").write_text(latex, encoding="utf-8")
    print(f"Wrote verified-generator summaries for {len(runs)} runs to {out_dir}")


if __name__ == "__main__":
    main()
